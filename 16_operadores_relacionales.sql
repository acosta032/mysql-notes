- Operadores relacionales
-- != - Diferente
-- <> - Diferente
-- = - Igual
-- > - Mayor que
-- >= - Mayor o igual que
-- < - Menor que
-- <= - Menor o igual que
-- NOT - Negar

-- Usuarios con edad distinta a 38
