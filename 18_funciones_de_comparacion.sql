* IS		Verifica un valor que puede tender a Booleano
* IS NOT	Verifica un valor que NO puede tender a Booleano
* IS NOT NULL	NOT NULL test
* IS NULL	NULL test
* ISNULL()	Verifica si el argumento es NULL
* LEAST()	Retorna el argumento mas pequenio
* NOT IN()	Whether a value is not within a set of values
* NOT LIKE	Negation of simple pattern matching
